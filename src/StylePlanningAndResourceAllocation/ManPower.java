package StylePlanningAndResourceAllocation;

/**
 *
 * @author Sampath Rajapakse
 */
public class ManPower {
    
    private int styleNo;
    private int direct;
    private int indirect;
    private int total;
    private int difference;
    private String type;

    /**
     * @return the styleNo
     */
    public int getStyleNo() {
        return styleNo;
    }

    /**
     * @param styleNo the styleNo to set
     */
    public void setStyleNo(int styleNo) {
        this.styleNo = styleNo;
    }

    /**
     * @return the direct
     */
    public int getDirect() {
        return direct;
    }

    /**
     * @param direct the direct to set
     */
    public void setDirect(int direct) {
        this.direct = direct;
    }

    /**
     * @return the indirect
     */
    public int getIndirect() {
        return indirect;
    }

    /**
     * @param indirect the indirect to set
     */
    public void setIndirect(int indirect) {
        this.indirect = indirect;
    }

    /**
     * @return the total
     */
    public int getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(int total) {
        this.total = total;
    }

    /**
     * @return the difference
     */
    public int getDifference() {
        return difference;
    }

    /**
     * @param difference the difference to set
     */
    public void setDifference(int difference) {
        this.difference = difference;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }
    
}
